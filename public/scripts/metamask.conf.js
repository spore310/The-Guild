window.addEventListener('load', async()=>{
    
    await loadWeb3();

    const buttonConnect = document.querySelector('#btn-wallet');
    
    buttonConnect.addEventListener("click", async () => {

        if (window.ethereum) {
            await ethereum.request({ method: 'eth_requestAccounts' });
            document.querySelector('#wallet').innerText = `Connected Wallet: ${ethereum.selectedAddress}`;
        }
    });


});

